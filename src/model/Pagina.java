public class Pagina {
    private int numPagina;
    private int tamaño;

    public Pagina(int numPagina, int tamaño) {
        this.numPagina = numPagina;
        this.tamaño = tamaño;
    }

    public int getNumPagina() {
        return numPagina;
    }

    public int getTamaño() {
        return tamaño;
    }
    
    

    
    
}
